import unittest
import parse_read_operation
import json


class ParseReadOperationTests(unittest.TestCase):
    def test_parse_recognition_results(self):
        parsed = json.loads(valid_response)
        parsed_data = parse_read_operation.parse_recognition_results(parsed['recognitionResults'])
        item = parsed_data[0]
        self.assertEqual(len(parsed_data), 3)
        self.assertEqual(item.page, 1)
        self.assertEqual(item.clockwiseOrientation, 349.59)
        self.assertEqual(item.pageWidth, 3200)
        self.assertEqual(item.pageHeight, 3200)
        self.assertEqual(item.unit, "pixel")
        self.assertEqual(item.text, "Our greatest glory is not")
        self.assertEqual(item.topLeftX, 202)
        self.assertEqual(item.topLeftY, 618)
        self.assertEqual(item.topRightX, 2047)
        self.assertEqual(item.topRightY, 643)
        self.assertEqual(item.bottomRightX, 2046)
        self.assertEqual(item.bottomRightY, 840)
        self.assertEqual(item.bottomLeftX, 200)
        self.assertEqual(item.bottomLeftY, 813)


